    /**
     * 형용사와 명사를 랜덤으로 조합하여 닉네임을 생성합니다.
     * @return 형용사와 명사가 조합된 랜덤 닉네임
     */
    fun generate(): String {
        val randomAdjective = adjectives[Random.nextInt(adjectives.size)]
        val randomNoun = nouns[Random.nextInt(nouns.size)]

        return "$randomAdjective $randomNoun"
    }
}
