package com.clip.global.util

import kotlin.random.Random

object NicknameGenerator {
    private val adjectives = listOf(
        "따듯한", "포근한", "다정한", "친절한", "든든한", "빛나는", "환한", "맑은", "푸른", "소중한",
        "즐거운", "행복한", "기쁜", "고요한", "아늑한", "밝은", "활기찬", "담백한", "정직한", "성실한",
        "여유로운", "사랑스런", "소박한", "자유로운", "향긋한", "활발한", "단단한", "온화한", "반가운", "푸근한",
        "정겨운", "달콤한", "졸린", "선량한", "청량한", "시원한", "희망찬", "선명한", "새벽의", "노을빛",
        "순수한", "진지한", "담담한", "고운", "깨끗한", "반짝이는", "향기로운", "화려한", "부끄러운"
    )

    private val nouns = listOf(
        "고양이", "강아지", "토끼", "사슴", "다람쥐", "햇살", "별빛", "달빛", "바람", "꽃잎",
        "나비", "새싹", "파도", "구름", "무지개", "장미", "코끼리", "펭귄", "돌고래", "은하수",
        "호수", "태양", "달님", "수국", "튤립", "판다", "여우", "기린", "부엉이", "하늘",
        "숲길", "바다", "샘물", "들꽃", "까치", "참새", "벌새", "노을", "아침", "반딧불",
